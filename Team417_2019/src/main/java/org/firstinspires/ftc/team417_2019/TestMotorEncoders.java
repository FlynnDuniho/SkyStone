/**
 * This program tests the motor encoders
 *

 */

@TeleOp(name="Test Encoders", group ="Test")

public class TestMotorEncoders extends MasterOpMode
{


    /**
     * This is the webcam we are to use. As with other hardware devices such as motors and
     * servos, this device is identified using the robot configuration tool in the FTC application.
     */
    WebcamName webcamName;

    @Override public void runOpMode() {



        /**
         * We use units of mm here because that's the recommended units of measurement for the
         * size values specified in the XML for the ImageTarget trackables in data sets. E.g.:
         *      <ImageTarget name="stones" size="247 173"/>
         * You don't *have to* use mm here, but the units here and the units used in the XML
         * target configuration files *must* correspond for the math to work out correctly.
         */
        float mmPerInch        = 25.4f;
        float mmBotWidth       = 18 * mmPerInch;            // ... or whatever is right for your robot
        float mmFTCFieldWidth  = (12*12 - 2) * mmPerInch;   // the FTC field is ~11'10" center-to-center of the glass panels

        /** Wait for the game to begin */
        telemetry.addData(">", "Press Play to start test");
        telemetry.update();
        initializeHardware();

        waitForStart();

        int posMotFL;
        int posMotFR;
        int posMotBL;
        int posMotBR;


        boolean buttonPressed = false;
        while (opModeIsActive()) {

            posMotFL = motorFL.getCurrentPosition();
            posMotFR = motorFR.getCurrentPosition();
            posMotBL = motorBL.getCurrentPosition();
            posMotBR = motorBR.getCurrentPosition();

            telemetry.addData("Motor Position", "%d %d %d %d", posMotBL, posMotBR, posMotFL, posMotFR);

            telemetry.update();
        }
    }

    /**
     * A simple utility that extracts positioning information from a transformation matrix
     * and formats it in a form palatable to a human being.
     */
    String format(OpenGLMatrix transformationMatrix) {
        return transformationMatrix.formatAsTransform();
    }



}
